public class ChattingClass {
    String profile;
    String text;

    public ChattingClass(String text) {

        this.text = text;
    }

    public String getProfile() {
        return profile;
    }

    public void setProfile(String profile) {
        this.profile=profile;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
